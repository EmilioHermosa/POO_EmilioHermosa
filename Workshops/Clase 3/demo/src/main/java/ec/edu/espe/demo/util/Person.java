package ec.edu.espe.demo.util;


public class  Person{

    private String name;
    private int age;

    public Person(){

    

    }

    public Person(String name2, int age){
    this.name = name2;
    this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

   

}

